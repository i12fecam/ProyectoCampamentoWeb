package Data;

/**
 * Este enumerado representa los tipos de inscripción para categorizar inscripciones.
 * Puede ser "TEMPRANA" o "TARDIA".
 *
 * - "TEMPRANA": Indica una inscripción temprana.
 * - "TARDIA": Indica una inscripción tardía.
 */
public enum TipoInscripcion {
    TEMPRANA,
    TARDIA
}
