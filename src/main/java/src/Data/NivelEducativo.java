package Data;

/**
 * Este enumerado representa los niveles educativos para categorizar a los participantes.
 * Puede ser "INFANTIL", "JUVENIL" o "ADOLESCENTE".
 *
 * - "INFANTIL": Indica un nivel educativo de tipo infantil.
 * - "JUVENIL": Indica un nivel educativo de tipo juvenil.
 * - "ADOLESCENTE": Indica un nivel educativo de tipo adolescente.
 */
public enum NivelEducativo {
    INFANTIL,
    JUVENIL,
    ADOLESCENTE
}
