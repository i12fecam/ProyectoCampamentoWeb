package Data;

/**
 * Este enumerado representa los posibles valores para el horario de una actividad o trabajo.
 * Puede ser "PARCIAL" o "COMPLETA".
 *
 * - "PARCIAL": Indica que la actividad o trabajo se realiza a tiempo parcial.
 * - "COMPLETA": Indica que la actividad o trabajo se realiza a tiempo completo.
 */
public enum Horario {
    PARCIAL,
    COMPLETA
}

